    case DAILY = "Daily"
    case WEEKLY = "Weekly"
    case BIWEEKLY = "Bi-Weekly"
    case MONTHLY = "Monthly"
    case QUARTERLY = "Quarterly"
    case YEARLY = "Yearly"
}
